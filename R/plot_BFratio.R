  mtext(side=1, "Expert 3", line=1.5, cex=1.3)
  boxBF(4, 1)
  mtext(side=1, "Expert 4", line=1.5, cex=1.3)
  boxBF(5, 1)
  mtext(side=1, "Expert 5", line=1.5, cex=1.3)
  boxBF(6, 1)
